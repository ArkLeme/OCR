#ifndef STRING_OPERATION
#define STRING_OPERATION

/*
 * Find the length of the string
 * s is the string
 * return the length
 */
int LengthStr(char* s);

/*
 * Concatene two string
 * s1 is the first string
 * s2 the second
 * return a new string
 */
char* Concatene(char* s1, char* s2);

/*
 * Find the length of an int
 * i is the int
 * return the length
 */
int LengthInt(int i);

/*
 * Reverse the string
 * s is the string
 * return the s
 */
char* Reverse(char *s);

/*
 * Convert int to string
 * i is the int
 * return the string
 */
char* Itoa(int i);

#endif
